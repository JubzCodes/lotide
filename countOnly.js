const countOnly = function (allItems, itemsToCount) {
  const obj = {};
  for (const names of allItems) {
    if (itemsToCount[names]) {
      if (obj.hasOwnProperty(names)) {
        obj[names] = obj[names] + 1
      } else {
        obj[names] = 1
      }
    }
  }
  return obj;
};

